function output = finding_Y(Y_dum)
Y = [Y_dum(1) Y_dum(2) Y_dum(3);
     Y_dum(4) Y_dum(5) Y_dum(6);
     Y_dum(7) Y_dum(8) Y_dum(9);
     Y_dum(10) Y_dum(11) Y_dum(12);
     Y_dum(13) Y_dum(14) Y_dum(15);
     Y_dum(16) Y_dum(17) Y_dum(18)];

C = [0.617255113405149,0,0,-0.448535555941661,0.134230346538703,1.41932930409004;0.283640603624291,0,2.22808153625508,-1.86462753317668,-1.99763494036701,-0.0116516666362024;0.350975702033898,-0.174162030842294,1.44167650824058,-0.392420914284623,1.30288133017051,-0.392171726620127];
E = [0.501297166526375;0.360896023286956;0.660595472942342;-0.936747952259017;-0.851919586460180;-0.669131067514653];

CEp=(C*E)'*((C*E)'*(C*E))^-1;
P1 = eye(6,6)-E*CEp*C;
P2 = (eye(3,3)-(C*E)*CEp)*C;
output1 = (P1+Y*P2)*E;
output = sumsqr(output1);
